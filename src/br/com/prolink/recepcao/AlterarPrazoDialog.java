    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(rbNao.isSelected()){
            salvar(null,false,null,documento_recebido);
        }
        else if(rbSim.isSelected() && jDateChooser1.getDate()!=null &&!"".equals(txMotivo.getText().trim())){
            salvar(txMotivo.getText(),true, jDateChooser1.getDate(), documento_recebido);
        }
        else if(jDateChooser1.getDate()==null)
            JOptionPane.showMessageDialog(null, "Data informada está incorreta!");
        else 
            JOptionPane.showMessageDialog(null, "Informe o motivo da alteração do prazo!");
    }//GEN-LAST:event_btnConfirmarActionPerformed

