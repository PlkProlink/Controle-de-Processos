    private void salvar(String observacao, boolean value, Date data, int documento_recebido){
        try{
            PreparedStatement ps;
            if(value){
                ps = getConnection().prepareStatement("update documentos_recebidos set devolver_ate=?,adiado=?, adiado_motivo=? where cod=?");
                ps.setDate(1, new java.sql.Date(data.getTime()));
                ps.setInt(2, 1);
                ps.setString(3, observacao);
                ps.setInt(4, documento_recebido);
                if(ps.executeUpdate()>0){
                    this.dispose();
                }
            }
            else{
                ps = getConnection().prepareStatement("update documentos_recebidos set passivel_devolucao=0 where cod=?");
                ps.setInt(1, documento_recebido);
                ps.executeUpdate();
                this.dispose();
            }
        }catch(SQLException e){
            e.printStackTrace();
        }finally{
            try{con.close();}catch(SQLException e){}
        }
    }


